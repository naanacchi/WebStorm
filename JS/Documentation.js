/**
 * @description returns multiplication of three numbers
 * @param {int} first component of multiplication
 * @param {int} second component of multiplication
 * @param {int} third component of multiplication
 * @return {int} multiplication of three components
 */
function mult(first, second, third)
{
    return first * second * third;
}
console.log(mult(2,4,6));